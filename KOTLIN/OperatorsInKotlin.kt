public fun main(){
    var a=10;
    var b=12;
    // arithmetic operators
    println(a+b) // addition
    println(a-b) // subtraction
    println(a*b) // multiplication
    println(a/b) // division
    // modulas
    println(a%b) // modulas
    // relational operators
    println(a>b)
    println(a<b)
    println(a>=b)
    println(a<=b)
    println(a==b)
    println(a!=b)

    //Increament and Decreament Operators
    println(++a)
    println(a++)
    println(--a)
    println(a--)
}